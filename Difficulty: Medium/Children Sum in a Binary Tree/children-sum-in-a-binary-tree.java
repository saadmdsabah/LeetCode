class Solution {
    
    public static boolean isValid(Node root){
        if(root == null){
            return false;
        }
        if(root.left != null && root.right != null){
            return (root.data == root.left.data + root.right.data) && isValid(root.left) && isValid(root.right);
        }else if(root.left != null){
            return (root.data == root.left.data) && isValid(root.left);
        }else if(root.right != null){
            return (root.data == root.right.data) && isValid(root.right);
        }else{
            return true;
        }
    }
    
    public static int isSumProperty(Node root) {
        return isValid(root) ? 1 : 0;
    }
}