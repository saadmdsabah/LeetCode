class Solution {
    public int countSubstring(String s) {
        HashMap<Integer, Integer> map = new HashMap<>();
        int count = 0;
        
        for(int i=0; i<s.length(); i++){
            int num = s.charAt(i) - 'a';
            if(map.containsKey(num)){
                map.put(num, map.get(num) + 1);
            }else{
                map.put(num, 1);
            }
            count += map.get(num);
        }
        return count;
    }
}